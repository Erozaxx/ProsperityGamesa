#!/usr/bin/env node
/**
 * Generates the versioned precache manifest consumed by the service worker.
 * Walks the repo for the static asset allowlist (app shell + core + vendor + ui + data + icons),
 * computes a content hash → cache version, writes src/precache.js (committed output).
 * Run manually after asset changes: node tools/gen-precache.mjs
 * Zero dependencies (node:fs, node:crypto, node:path only).
 */

import { readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs';
import { createHash } from 'node:crypto';
import { join, resolve as pathResolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const ROOT = pathResolve(__dirname, '..');

/** Roots to include in the precache (relative to repo root). */
const ROOTS = [
  'index.html',
  'manifest.webmanifest',
  'icons',
  'src/app',
  'src/ui',
  'src/vendor',
  'src/core',
  'src/data',
];

/** Patterns to exclude from precache (tests, types, docs, gitkeep). */
const EXCLUDE = [
  /\.test\.js$/,
  /\.gitkeep$/,
  /\.d\.ts$/,
  /\.md$/,
  /\.map$/,
];

/**
 * @param {string} filePath absolute path
 * @returns {boolean}
 */
function shouldExclude(filePath) {
  const rel = filePath.slice(ROOT.length + 1).replace(/\\/g, '/');
  return EXCLUDE.some((rx) => rx.test(rel));
}

/**
 * Recursively walk a directory and return all files.
 * @param {string} dir
 * @returns {string[]}
 */
function walkDir(dir) {
  /** @type {string[]} */
  const results = [];
  let entries;
  try {
    entries = readdirSync(dir);
  } catch {
    return results;
  }
  for (const entry of entries) {
    const full = join(dir, entry);
    let stat;
    try {
      stat = statSync(full);
    } catch {
      continue;
    }
    if (stat.isDirectory()) {
      results.push(...walkDir(full));
    } else {
      results.push(full);
    }
  }
  return results;
}

/**
 * Collect all precache-eligible files.
 * @returns {string[]} sorted relative paths (./prefixed)
 */
function collectFiles() {
  /** @type {string[]} */
  const files = [];

  for (const root of ROOTS) {
    const full = join(ROOT, root);
    let stat;
    try {
      stat = statSync(full);
    } catch {
      // Root doesn't exist yet (e.g. src/data is empty) – skip
      continue;
    }

    if (stat.isDirectory()) {
      for (const f of walkDir(full)) {
        if (!shouldExclude(f)) {
          const rel = './' + f.slice(ROOT.length + 1).replace(/\\/g, '/');
          files.push(rel);
        }
      }
    } else {
      if (!shouldExclude(full)) {
        files.push('./' + root.replace(/\\/g, '/'));
      }
    }
  }

  files.sort();
  return files;
}

/**
 * Compute sha256 hash over file paths + contents (deterministic, content-based – not mtime).
 * @param {string[]} files relative paths (./prefixed)
 * @returns {string} hex digest
 */
function computeHash(files) {
  const hash = createHash('sha256');
  for (const rel of files) {
    const abs = join(ROOT, rel.slice(2)); // strip './'
    let content;
    try {
      content = readFileSync(abs);
    } catch {
      content = Buffer.alloc(0);
    }
    hash.update(rel);
    hash.update(':');
    hash.update(content);
  }
  return hash.digest('hex');
}

/**
 * Main entry point (also exported for testing).
 * @param {Object} [opts]
 * @param {string} [opts.outFile] - output path (default: src/precache.js in ROOT)
 * @returns {{ files: string[], version: string }}
 */
export function generatePrecache(opts = {}) {
  const outFile = opts.outFile ?? join(ROOT, 'src', 'precache.js');
  const files = collectFiles();
  const hex = computeHash(files);
  const version = 'prosperity-' + hex.slice(0, 12);

  const output = [
    '// AUTO-GENERATED by tools/gen-precache.mjs — DO NOT EDIT',
    `export const PRECACHE_VERSION = '${version}';`,
    `export const PRECACHE_URLS = [`,
    ...files.map((f) => `  '${f}',`),
    '];',
    '',
  ].join('\n');

  writeFileSync(outFile, output, 'utf8');
  return { files, version };
}

// Run as CLI
if (process.argv[1] && fileURLToPath(import.meta.url) === pathResolve(process.argv[1])) {
  const { files, version } = generatePrecache();
  console.log(`precache: ${files.length} files, version ${version}`);
}
